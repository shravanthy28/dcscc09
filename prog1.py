# A simple Python program

# Ask for user's name and age
name = input("What is your name? ")
age = int(input("How old are you? "))

# Calculate age next year
age_next_year = age + 1

# Print the message
print(f"Hello, {name}! You are currently {age} years old.")
print(f"Next year, you'll be {age_next_year} years old!")
